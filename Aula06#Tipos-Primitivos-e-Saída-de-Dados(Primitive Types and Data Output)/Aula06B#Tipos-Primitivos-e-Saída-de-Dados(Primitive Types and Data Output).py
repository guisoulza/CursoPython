# Tipos Primitivos e Saída de Dados (Primitive Types and Data Output).


nf=float(input('Digite um valor: '))                      # Dado do tipo float para números decimais ou ponto flutuante.
print(type(nf))                                                 # Data type float for decimal or floating-point numbers.
print(nf)
ns=str(input('Digite um valor: '))                                                # Dado do tipo string para caracteres.
print(type(ns))                                                                       # Data type string for characters.
print(ns)
nb=bool(input('Digite um valor: '))              # Dado do tipo bool para valores verdadeiros ou falsos (True ou False).
print(type(nb))                                               # Data type bool for true ou false values (True or false).
print(nb)
ni=int(input('Digite um valor: '))                                         # Dado do tipo inteiro para números inteiros.
print(type(ni))                                                                       # Data type int for whole numbers.
print(ni)



