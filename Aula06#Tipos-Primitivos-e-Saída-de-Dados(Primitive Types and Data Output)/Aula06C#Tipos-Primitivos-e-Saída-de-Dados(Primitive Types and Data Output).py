# Tipos Primitivos e Saída de Dados (Primitive Types and Data Output).

# Formas de usar o tipo booleano (Ways to use the bool type).

n=input('Digite algo: ')
print(f'É {n.isnumeric()}')                 #Nesta linha o .isnumeric() vai mostrar se o dado é um número.
print(f'É {n.isalpha()}')                   #Nesta linha o .isalpha() vai mostrar se o dado possui letras.
print(f'É {n.isalnum()}')                   #Nesta linha o .isalnum() vai mostrar se o dado tem números ou letras.

# In line 6, .isnumeric() will check if the data have number.
# In line 7, .isalpha() will check if the data have letters.
# In line 8, .isalnum() will check if the data have numbers ou letters.

